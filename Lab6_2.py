a = int(input("Podaj pierwszą liczbę całkowitą: "))
b = int(input("Podaj drugą liczbę całkowitą: "))

print("Wynik dodawania wprowadzonych liczb całkowitych to: ", a+b)
print("Wynik odejmowania wprowadzonych liczb całkowitych to: ", a-b)
print("Wynik dzielenia wprowadzonych liczb całkowitych to: ", a/b)
print("Wynik mnożenia wprowadzonych liczb całkowitych to: ", a*b)
print("Wynik dzielenia modulo wprowadzonych liczb całkowitych to: ", a%b)